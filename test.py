def calculate_area(length: float, width: float) -> float:
    """
    Calculate the area of a rectangle.

    Args:
        length (float): The length of the rectangle. Represents the horizontal dimension.
        width (float): The width of the rectangle. Represents the vertical dimension.

    Returns:
        float: The area of the rectangle.
    """
    return length * width


